export function getTourBySlug(slug: string): Tour | undefined {
  return tours.find((t) => t.slug === slug);
}

export function getToursByCategory(categorySlug: string): Tour[] {
  return tours.filter((t) => t.categories.includes(categorySlug));
}

export function getRelatedTours(tour: Tour): Tour[] {
  return tour.relatedSlugs
    .map((slug) => getTourBySlug(slug))
    .filter((t): t is Tour => t !== undefined);
}
